				Originally VOAU was the acronym of a Minecraft save I had from my youth.  This was a world that I had anticipated to use as a local multiplayer save, but eventually the game crashed due to lack off sufficient RAM and I lost the save forever.  The world was originally Aaron's creation, but then I expanded to create more diverse characters.  I took the characters I put together and created one world where they all could connect together in their own stories, and from there expanded it into their own world.  Their worlds initially were forged out of what inspired me in creating them.  The idea is forged similar to present day but with serious scientific and technological advancements, a different zeitgeist, and international conflicts both reminiscent and new.  This is controversial, but makes it easier to forge the story in your own time period; one can notice how novels, movies, and games portray the future as if society has made great progress in the areas listed above but this world is slightly more close to home. Slightly.
			</p>
