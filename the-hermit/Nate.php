			<p>
				<br><br>
				Born at an unknown time in Texas, not much is known of his life before encountering Aaron.  The two met when they both enlisted into the military, later getting more involved in it.  After both of them losing their wives in a freak accident. Nate became upset, and would argue with Aaron.  To this, Nate left Colorado and moved back to Texas.  Before he did so and thanks to the scientific research Aaron did about his father, he made Nate invulnerable to time as well.  
				<br>
				<br>
				In the time between their separation as friends and the next subsequent reintegration into this common society Nate frequented drift racing under an alias which he would then teach Aki about.
				<br>
				<br>
				As the two immortal friends both outlasted time, Nate got a strange call in December of 2085.  This was regarding an operation The Anger had been led to by her late, unknown accomplice.  After meeting secretively in Tokyo, Japan, Nate found out The Anger to be one Ren Kimura.  The two attempted a relationship, which ended at Ren killing Nate after discovering his involvement with Aaron. Soon after he had been brought back from death by Aaron and sat alone in his own home.
				<br>
				<br>
				Nate realized the fault of having relationships with potential psychopaths, and wanted to get back into military action again.  In doing so he found his way into groups sent out to eliminate the ZEDs from Europe.  After failure of entering Europe on an initial try, Scully got himself involved in trying to get Nate to Europe.  Then Ren applied to the program and made her way to Europe.  Nate was soon abandoned by their escape plan and was left alone.
				<br>
				<br>
				Without anything to do, he outlasted nearly a month alone until he was confronted by Kari along with her two surviving friends Dominic and Marius.  After traveling with them long enough a still unknown culprit mortally wounded Dominic and Marius, with Kari left with nothing but to kill them out of mercy as the alternative was much worse. With no one else left to turn to Kari grew to fall in love with Nate.  Soon accepting the direction of the situation, he gave in and started a relationship with her cautiously. It was soon that Nate would realize that the situation wouldn't be so bad after all.
				<br>
				<br>
				Kari and Nate both soon escaped Europe weeks before the Northern War of Ukraine, which would eliminate all ZEDs from the world outright.  Nate and Kari lived in Miami, Florida happily enough until Nate crossed paths yet again with Ren, but this time also with Aki and Jacob.  He took Kari and what little they had and left Florida for good.  After driving across the United States to the west coast, Nate got a call from Aki in apology and admiration, offering him to join to work with her and her new found friends Ashton and Alex Mason.
				<br>
				<br>
				It would be a tumultuous next few years living in California as Aki declared it separate from the United States indefinitely, Labs would make an unnecessarily belligerent reappearance, and work experience would earn Nate his infamy in such events like Code Silver: Chaos and Operation Blacklight. Many times would Kari's broken mind force arguments back to him, giving Nate a dreaded feeling that reminded him why he'd even bother with relationships after his late wife's situation, making him seem more apathetic towards Kari. He would still follow Kari back to Germany after its reconstruction because like her he felt as if they only had each other left as all they loved before were dead.
				<br>
				<br>
				Nate never ceased to be the center of attention as he unintentionally meddled with more than he could manage in an attempt to follow curiosity, later revealing the details of the existence of Avis Ardoris and the other Old Gods. He would be on trial for a crime he did not commit, he would be there to convince Kari not to commit suicide, and he wouldn't have known it over time that Kari would influence him more than anyone else ever. Nate became a better person as Kari realized she too carried a sense of partial immortality, developing from apathetic immaturity to being a true legendary hero.
				<br>
				<br>
				The true test of Nate's resolve as he learned from a woman he never knew could teach him anything was how to be a parent. As Voce found himself opportunities to visit the dimension he was truly born in Nate found himself as a father figure yet again, despite Voce already way past the point of adulthood. Nate would have two last tests, that of himself again as a father and trying to find Kari in "the Aether" to seek salvation in her where they only had each other.
