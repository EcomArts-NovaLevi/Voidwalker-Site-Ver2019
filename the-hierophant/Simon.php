			<p>
				<br><br>
				Little to nothing is known of Dr. Peterson's origins, except that he had an unhealthy and introverted social life during school and had gone off to study health and medical science.  At a young age of 27, out of college and into his career he went.  The short few years he had at the hospital he worked at, he enjoyed despite the hard work put into it. 
				<br>
				<br>
				On his off-time, Dr. Peterson spent the time adjusting to the short breaks by doing quick medical sketches of virus microbes, injuries, and whatever people he had attended to dealt with in the day.  He wore through countless sketchbooks and sketchpads, one day finding he had forgotten to purchase a new one before a shift at his hospital. Dr. Peterson found by chance on his commute to work at a garage sale. There sat one which had been white on the front with a black minus (-) and black on the back with a white plus (+) and surprisingly unused entirely.  The design appealed to him, and he purchased it.
				<br>
				<br>
				Upon opening the sketchpad for the first time Dr. Peterson noticed something behind the last page that he didn't when purchasing it. The last page hid a list of names with years written to the right dating back into the 1950's. Dr. Peterson was for once perplexed, keeping the list at his desk at home planning to study it further. This led to his discovery that the people listed had vanished after buying or using the sketchpad, but too late to take back that he had too Dr. Peterson was prepared to meet whatever strange fate he had ignorantly fallen into.  
				<br>
				<br>
				Weeks passed, and then months, but to Dr. Peterson's surprise he was not ever spirited away or taken. Instead he encountered strange changes in himself in that time. His eyes grew green, being able to see in the dark; and his strength increased with him able to pull doors off their hinges and break metal like it were weak glass.  With his vision enhancements he was able to able to see the two sides the sketchpad who he would later dub as Positive and Negative. "The Foundation", as Dr. Peterson would always call it, was onto him at this point. Absent from work, seemingly delusional, and speaking of ethereal beings similar to those mentioned by those who did disappear from the sketchpad in the past. At this point the Foundation had finally found a lost anomalous object, then subduing and capturing Dr. Peterson with it.
				<br>
				<br>
				The Foundation quickly learned that the "Sentient" Sketchpad was indeed a lost item linking to past reports of disappearances.  They thought the sketchpad also made Dr. Peterson more violent and aggressive, making people feel timid around him, but in reality that was his response to his imprisonment.  In further research to Dr. Peterson's sketchpad, the Foundation has made further action to cut Simon from reality including erasing all record of him and wiping the memory of him from relatives of his.
