				<br><br>
				Voce D'oro, meaning Voice of Gold in Italian, is made with clarity with his aid but enigma in his nature.  Largely independent he keeps his true intentions to himself largely but is exceptionally good at pointing out others' secrets and hiding his simultaneously.  Regardless of this he lends his trust and skills to those that benefit them naturally, making him a reliable ally.  Overall he is rather neutral to most of what goes on in life and focuses instead on his personal endeavors.  From a past experience that left a scar in his timeline he wishes to join the mafia only to dismantle it from the inside.  This is his primary goal and will not stop at anything to prove his end goal true and prove to gang allies that he can be perfectly trusted.  Ensuring everyone's trust by doing what he can to ensure their cooperated success.
				<br><br>
				The other important personal quest Voce wishes to fulfill regards his past.  He lacks an understanding of his origins and true family, naturally making him a loner in his recent travels.  Because of this he is already naturally experienced even at the age of 16, and has even bonded well with a strong ability tied to his soul.  
				<br><br>
				He left what he thought to be his parents at the age of 15 when this ability first came to be.  Upon hearing this his parents mentioned their adoption of him to him and gave him a single photo that they were told to give to him if and when he mentioned anything of a bound ghost or evil spirit.  He left the family farm the next morning to pursue answers to what happened in such short notice.
				<br><br>
				There's something he can't answer specifically and that is where he comes from.  He does not know who his real family is but over time has accumulated enough information to point him in the direction of the photo.  He treasures this photo as it his is only lead to understanding who he is.  The picture itself is only labeled as "Kari" and its contents are blurry as if taken at a distance but depict a female; white, also with blue eyes, blonde hair, and a height looking to be similar to Voce's.  When asking around for her full identity people tell stories of a spectacular hero pulling off the impossible to aid those in need and vanquish evil, all talking about her success attributed to unwavering resolve.  On the back of the photo is written in pen, "Rider of the Sky".
				<br><br>
				Voce takes the point of resolve to heart and wields it with him, and attributes his successes to Kari's.  Although he has fallen into the visage of a criminal due to what he does for the greater good, he feels it does not matter as his intentions mean good.  Voce is inspired by what he hears of Kari and uses those stories to fulfill his resolve as he too hopes to satisfy his personal needs and give back to the world.
				<br><br>
				Voce shares his physical features, personal ties, inherent leadership, and the upbringing of and ability like Rider of the Sky along with much more to a not so coincidental degree with Kari. In a strange understanding of time and space, Voce is the first born child and son of Kari Frost with details of a father rather strange. Voce was born to Kari in an unconventional manner as he was simply "birthed" into life spontaneously following an altercation with Katya and her control over the world in everyone's natural dimension, thus genetically Voce's parents are Kari and Katya. Kari at first had no intention of raising a child that had anything to do with Katya, also frustrated further that Katya limited Kari to functionally sterile without her soul, and wished to give the baby boy off to parents in need of a child in a different dimension all together. As not to feel soulless and irresponsible, Kari wanted to give herself time to cope with the conflict and time for the child to grow old enough to understand her conflict. When she was ready Kari would seek out Voce and hope to discuss her side of the debacle and let Voce's now developed, adult mind help determine a solution between the two of them.
				<br><br>
				Upon finding suitable parents in a dimension on a planet so vast, where eye color dictated class, she sent a courier who instructed the parents that the boy may one day in his late teens mention something about a "bound ghost" or "evil spirit" that he can control, and when he does so to give him a photo taken of her in the Aether that she gave them. When prompted as to if the boy had a name the courier said he was Voce D'oro, which back in the natural dimension Kari felt he would take after her. Little did Kari know that Voce would venture to seek her out on his own on an adventure that led him to the truth behind her, Katya, and both the abilities of Rider of the Sky and Sweet Dreams Requiem. Also did Kari not realize was that their shared eye color of blue would make Voce appear already out of place as a high class aristocratic boy on a farm in a middle class city area.
