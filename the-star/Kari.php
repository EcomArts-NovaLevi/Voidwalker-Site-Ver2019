			<p>
				<br><br>
				Kari is the main character of VOAU/Voiwalker. All references to "Stygian" refer to all there is of her after a serious change in plot for the future of VOAU/Voidwalker but further down the line is still be referred to as Kari.
				<br><br>
				Kari Frost, quite a unique name, was born in Leipzig, Germany.  She grew up in a boring life being a basic person, as she claims, like a "nobody" or a "grunt".  Taking student exchange programs to have schooling in America, Kari was one a few lucky people to try this out, and was dissatisfied by America's lethargy and lack of care of other countries.
				<br>
				<br>
				Instead of doing too much into college, Kari wanted to support her country militantly.  Though being a police officer at a questionably young age, she experienced an event which greatly presented her "mind over matter" mindset to an interested few individuals.  Eventually, she made her way into Grenzschutzgruppe 9 (GSG-9).  Doing so forced her to move to the Federal City of Bonn.
				<br>
				<br>
				While being in GSG-9 she has earned permanent scars of a knife to the left shoulder and a slash down her right arm from a reckless knife fight.  When the nasties hit Europe, she and two other operatives lasted a year in the remnants of Germany.
				<br>
				<br>
				Later to be found by Nate, Ren, and Scully (in that order) Kari set on a plan of escape, which in a pyrrhic victory she had lost her colleagues Marius and Dominic to still unknown reasons.  Ignorant of American life other than the unpleasant life she had in the exchange program, she was left to decide which state to reside in when she and Nate had taken refuge in America.  Kari had chosen Florida for it's fairly warm weather and paradise hearsay, to which Nate hesitantly agreed.
				<br>
				<br>
				For a very long time Kari spent her time somewhat wasting away with Nate's wealth in California after their retreat from Florida. Kari had this time to get used to everyone she'd be involved with for the rest of her life and at first saw no interest in it. Spending a few years relatively doing nothing, as well as reenlisting in a reformed CTU service with many other high profile military experienced characters (including Nate), Kari saw no part in staying in America any further. With her past ripped abruptly from her leading her to a future she didn't want, Kari developed depression over her feeling that she had every chance to prevent her two best friends' deaths but instead had to be the one to end them out of pity.
				<br>
				<br>
				Once the redevelopment of Europe after the collapse passed the point of becoming inhabitable again Kari angrily moved back to her old home and offered Nate a place in it if he truly wished her well. He did move back with her for some time before Kari wanted only to isolate herself and sent him back to California. The despair brought to her over years of demoralizing service eventually brought her to a point of no return, which then her depression caught up with her.
				<br>
				<br>
				Upon attempting to end her life with Nate trying his hardest to convince her not to, Katya finally took initiative of her interest in Kari's life experiences and stopped the suicide attempt in its tracks. From that point on was a dreadful curiosity that loomed over Kari's head until it lead her to the infamous event that let Katya claim Kari's soul to ensure Kari's eternal life.
				<br>
				<br>
				Initially Kari was spiteful of Katya over the first several decades being technically undead, until after a long 50~60 years Katya finally took initiative to explain her intentions peacefully. Something within Kari reminded her of her temperament in her youth that day, it reminded her of doing the right thing and being the hero she wanted others in the world to have. Almost unrealistically Kari agreed to see things in a kinder perspective for Katya. From then on Kari rose as a light shining in darkness, vanquishing evil with a resolve brighter than sunup at dawn. It was at that point that Kari learned again to smile and carry on.
				<br>
				<br>
				While most of Kari's time is spent aiding those in need across different dimensions with her Sweet Dreams Requiem, she does return back to her home dimension dubbed "World Zero" doing the same for those she previously despised. Along side building a better reputation with her now many friends and acquaintances in World Zero, Kari has done the same in other dimensions she prefers to frequent. This, however, is not full of consequences as one may expect as she has established a life for her son in one dimension as she has built an inseparable bond with new friends in another.
